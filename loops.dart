void main() {

  /* Loops are of three types:
  1. For loop
  2. While loop
  3. Do-While loop
  */

  //case 1: For Loop
  for(int i=0;i<10;i++) {
    print("$i");
  }

  //case 2: While Loop
  var i=0;
  while(i<10) {
    print(i);
    i++;
  }

  //case 3: Do While Loop
  i=0;
  do {
    print(i);
    i++;
  } while(i!=10);

}